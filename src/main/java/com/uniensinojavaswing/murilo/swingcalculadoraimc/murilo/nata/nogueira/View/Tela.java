    /**
     * @param args the command line arguments
     */
    private void efetuarConversao(double valorInserido){
        String moedaDestinoSelecionada = (String)comboDestino.getSelectedItem();
        String moedaOrigemSelecionada = (String)comboOrigem.getSelectedItem();
        
        String chave = moedaOrigemSelecionada + "_TO_" + moedaDestinoSelecionada;
        if(moedaDestinoSelecionada.equals(moedaOrigemSelecionada)){
            chave = "EQUALS";
        }
        
        if(mapaConversoes.containsKey(chave))
        {
            double taxa = mapaConversoes.get(chave);
            double conversao = valorInserido * taxa;
            
            String conversaoResultado = String.format("%.2f %s",conversao, moedaDestinoSelecionada);
            valorConvertido.setText(conversaoResultado);
        }
    }

