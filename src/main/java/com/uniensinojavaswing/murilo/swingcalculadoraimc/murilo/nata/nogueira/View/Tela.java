    private void btnConverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConverterActionPerformed
        if(textFieldValor.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Porfavor coloque um valor decimal para converter");
        } else {
            try{
                double valorMoeda = Double.parseDouble(textFieldValor.getText());
                if(valorMoeda < 0) {
                    JOptionPane.showMessageDialog(this, "Número inválido, coloque números decimais positivos");
                } else {
                    efetuarConversao(valorMoeda);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Formatação inválida, coloque números decimais positivos");
            }
            
        }
            
    }//GEN-LAST:event_btnConverterActionPerformed

